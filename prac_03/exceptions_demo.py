try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Cannot divide by zero!")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
print("Finished.")

# 1. A ValueError occurs when the user inputs something that cannot be converted to an integer.
# 2. A ZeroDivisionError occurs when the user enters 0 as the denominator.
# 3. To avoid the ZeroDivisionError, you can use a loop to repeatedly ask for a non-zero denominator.